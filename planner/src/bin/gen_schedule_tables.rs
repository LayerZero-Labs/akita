fn usage() -> &'static str {
    "usage: cargo run -p hachi-planner --bin gen_schedule_tables -- <output-dir> [--family <name>]..."
}

fn family_by_name(name: &str) -> Option<FamilySpec> {
    ALL_FAMILIES
        .iter()
        .copied()
        .find(|family| family.family_name == name)
}

fn emit_shape(shape: &DirectWitnessShape) -> String {
    match shape {
        DirectWitnessShape::PackedDigits {
            num_elems,
            bits_per_elem,
        } => format!(
            "GeneratedDirectWitnessShape::PackedDigits {{ num_elems: {num_elems}, bits_per_elem: {bits_per_elem} }}"
        ),
        DirectWitnessShape::FieldElements { num_elems } => format!(
            "GeneratedDirectWitnessShape::FieldElements {{ num_elems: {num_elems} }}"
        ),
    }
}

fn emit_family_module(spec: FamilySpec) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "// Generated by `cargo run -p hachi-planner --bin gen_schedule_tables -- <output-dir>`");
    let _ = writeln!(out, "#[derive(Clone, Copy, Debug, PartialEq, Eq)]");
    let _ = writeln!(out, "pub enum GeneratedDirectWitnessShape {{");
    let _ = writeln!(out, "    PackedDigits {{ num_elems: usize, bits_per_elem: u32 }},");
    let _ = writeln!(out, "    FieldElements {{ num_elems: usize }},");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "#[derive(Clone, Copy, Debug, PartialEq, Eq)]");
    let _ = writeln!(out, "pub struct GeneratedFoldStep {{");
    let _ = writeln!(out, "    pub current_w_len: usize,");
    let _ = writeln!(out, "    pub d: u32,");
    let _ = writeln!(out, "    pub log_basis: u32,");
    let _ = writeln!(out, "    pub m_vars: u32,");
    let _ = writeln!(out, "    pub r_vars: u32,");
    let _ = writeln!(out, "    pub n_a: u32,");
    let _ = writeln!(out, "    pub n_b: u32,");
    let _ = writeln!(out, "    pub n_d: u32,");
    let _ = writeln!(out, "    pub delta_open: usize,");
    let _ = writeln!(out, "    pub delta_fold: usize,");
    let _ = writeln!(out, "    pub delta_commit: usize,");
    let _ = writeln!(out, "    pub w_ring: usize,");
    let _ = writeln!(out, "    pub next_w_len: usize,");
    let _ = writeln!(out, "    pub level_bytes: usize,");
    let _ = writeln!(out, "    pub label: &'static str,");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "#[derive(Clone, Copy, Debug, PartialEq, Eq)]");
    let _ = writeln!(out, "pub struct GeneratedDirectStep {{");
    let _ = writeln!(out, "    pub current_w_len: usize,");
    let _ = writeln!(out, "    pub witness_shape: GeneratedDirectWitnessShape,");
    let _ = writeln!(out, "    pub entry_d: Option<u32>,");
    let _ = writeln!(out, "    pub entry_nb: Option<u32>,");
    let _ = writeln!(out, "    pub direct_bytes: usize,");
    let _ = writeln!(out, "    pub total_bytes: usize,");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "#[derive(Clone, Copy, Debug, PartialEq, Eq)]");
    let _ = writeln!(out, "pub enum GeneratedStep {{");
    let _ = writeln!(out, "    Fold(GeneratedFoldStep),");
    let _ = writeln!(out, "    Direct(GeneratedDirectStep),");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "#[derive(Clone, Copy, Debug, PartialEq, Eq)]");
    let _ = writeln!(out, "pub struct GeneratedScheduleEntry {{");
    let _ = writeln!(out, "    pub max_num_vars: usize,");
    let _ = writeln!(out, "    pub total_bytes: usize,");
    let _ = writeln!(out, "    pub steps: &'static [GeneratedStep],");
    let _ = writeln!(out, "}}");
    let _ = writeln!(out);
    let _ = writeln!(out, "#[rustfmt::skip]");
    let _ = writeln!(
        out,
        "pub static {}: &[GeneratedScheduleEntry] = &[",
        spec.const_name
    );

    for nv in spec.min_num_vars..=spec.max_num_vars {
        let mut opts = PlannerOptions::new(spec.log_commit_bound, nv);
        opts.ring_configs = spec.ring_configs;
        let sched = run_universal_planner(&opts);
        let _ = writeln!(
            out,
            "    GeneratedScheduleEntry {{ max_num_vars: {nv}, total_bytes: {}, steps: &[",
            sched.total_bytes
        );
        for step in &sched.steps {
            match step {
                PlannedStep::Fold(level) => {
                    let _ = writeln!(
                        out,
                        "        GeneratedStep::Fold(GeneratedFoldStep {{ current_w_len: {}, d: {}, log_basis: {}, m_vars: {}, r_vars: {}, n_a: {}, n_b: {}, n_d: {}, delta_open: {}, delta_fold: {}, delta_commit: {}, w_ring: {}, next_w_len: {}, level_bytes: {}, label: {:?} }}),",
                        level.current_w_len,
                        level.d,
                        level.lb,
                        level.m_vars,
                        level.r_vars,
                        level.na,
                        level.nb,
                        level.nd,
                        level.delta_open,
                        level.delta_fold,
                        level.delta_commit,
                        level.w_ring,
                        level.next_w_len,
                        level.level_bytes,
                        level.label,
                    );
                }
                PlannedStep::Direct(direct) => {
                    let _ = writeln!(
                        out,
                        "        GeneratedStep::Direct(GeneratedDirectStep {{ current_w_len: {}, witness_shape: {}, entry_d: {:?}, entry_nb: {:?}, direct_bytes: {}, total_bytes: {} }}),",
                        direct.current_w_len,
                        emit_shape(&direct.witness_shape),
                        direct.entry_d,
                        direct.entry_nb,
                        direct.direct_bytes,
                        direct.total_bytes,
                    );
                }
            }
        }
        let _ = writeln!(out, "    ] }},");
    }

    let _ = writeln!(out, "];");
    out
}

fn main() -> Result<(), String> {
    let mut args = env::args().skip(1);
    let Some(output_dir) = args.next() else {
        return Err(usage().to_string());
    };
    let mut requested_families = BTreeSet::new();
    while let Some(arg) = args.next() {
        if arg == "--family" {
            let Some(name) = args.next() else {
                return Err(usage().to_string());
            };
            if family_by_name(&name).is_none() {
                return Err(format!(
                    "unknown family {name:?}; expected one of: {}",
                    ALL_FAMILIES
                        .iter()
                        .map(|family| family.family_name)
                        .collect::<Vec<_>>()
                        .join(", ")
                ));
            }
            requested_families.insert(name);
            continue;
        }
        return Err(usage().to_string());
    }

    let output_dir = PathBuf::from(output_dir);
    fs::create_dir_all(&output_dir)
        .map_err(|err| format!("failed to create {}: {err}", output_dir.display()))?;

    let families: Vec<FamilySpec> = if requested_families.is_empty() {
        ALL_FAMILIES.to_vec()
    } else {
        requested_families
            .into_iter()
            .map(|name| family_by_name(&name).expect("requested family was prevalidated"))
            .collect()
    };

    for family in families {
        let dest = output_dir.join(format!("{}.rs", family.module_name));
        let body = emit_family_module(family);
        fs::write(&dest, body).map_err(|err| format!("failed to write {}: {err}", dest.display()))?;
        println!("wrote {}", dest.display());
    }

    Ok(())
}
