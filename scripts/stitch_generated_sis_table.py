#!/usr/bin/env python3
"""Regenerate and stitch `crates/akita-types/src/sis/generated_sis_table/`."""

from __future__ import annotations

import argparse
import re
import shutil
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "scripts"))
from gen_sis_table import RING_DIMS, derived_l2_collision_keys  # noqa: E402

OUT_DIR = ROOT / "crates/akita-types/src/sis/generated_sis_table"
LEGACY_OUT = ROOT / "crates/akita-types/src/sis/generated_sis_table.rs"
GEN = ROOT / "scripts/gen_sis_table.py"
FAMILIES = ("q32", "q64", "q128")
FAMILY_ENUM = {"q32": "Q32", "q64": "Q64", "q128": "Q128"}
ARM_RE = re.compile(r"^\((\d+), (\d+)\) => Some\(&\[(.*?)\]\),$")

TABLE_HEADER = """// AUTO-GENERATED by scripts/stitch_generated_sis_table.py — do not edit by hand.
//
// SIS width thresholds for 128-bit security (BDGL16, Euclidean norm=2).
// Keys are the union of:
//   - power-of-two squared-collision buckets (2^MIN_LOG_BUCKET .. 2^MAX_LOG_BUCKET)
//   - derived keys K = d · B² for coefficient-L∞ buckets B (norm_bound default)
// See `MIN_LOG_BUCKET` / `MAX_LOG_BUCKET` / `COEFF_LINF_BUCKETS` in `sis/ajtai_key.rs`.
//
// Regen (full):
//   git submodule update --init third_party/lattice-estimator
//   sage -python scripts/stitch_generated_sis_table.py --jobs 6
//
// Regen derived rows only (merge into existing table):
//   sage -python scripts/stitch_generated_sis_table.py --supplement-derived-only --jobs 6
"""


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--jobs", type=int, default=6)
    parser.add_argument("--max-rank", type=int, default=20)
    parser.add_argument(
        "--supplement-derived-only",
        action="store_true",
        help=(
            "Regenerate only derived d·B² rows and merge into the existing "
            "generated table (skip the full power-of-two sweep)."
        ),
    )
    parser.add_argument(
        "--split-existing",
        action="store_true",
        help="Split the legacy monolithic generated_sis_table.rs without Sage regen.",
    )
    parser.add_argument("--dry-run", action="store_true")
    return parser.parse_args()


def run_family(
    family: str,
    jobs: int,
    max_rank: int,
    *,
    collisions: list[int] | None = None,
) -> str:
    cmd = [
        "sage",
        "-python",
        str(GEN),
        "--family",
        family,
        "--jobs",
        str(jobs),
        "--max-rank",
        str(max_rank),
        "--format",
        "rust",
    ]
    if collisions is not None:
        cmd.extend(
            [
                "--dims",
                ",".join(str(d) for d in RING_DIMS),
                "--collisions",
                ",".join(str(c) for c in collisions),
            ]
        )
    print(f"running: {' '.join(cmd)}", file=sys.stderr)
    proc = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, check=False)
    if proc.returncode != 0:
        raise SystemExit(f"{family} regen failed:\n{proc.stderr}")
    return proc.stdout


def parse_arms(block: str) -> dict[tuple[int, int], str]:
    arms: dict[tuple[int, int], str] = {}
    for line in block.splitlines():
        stripped = line.strip()
        match = ARM_RE.match(stripped)
        if match is None:
            continue
        d = int(match.group(1))
        collision = int(match.group(2))
        arms[(d, collision)] = stripped
    return arms


def read_table_source() -> str | None:
    if LEGACY_OUT.exists():
        return LEGACY_OUT.read_text()
    mod_rs = OUT_DIR / "mod.rs"
    if mod_rs.exists():
        return mod_rs.read_text()
    return None


def parse_existing_family_arms(content: str, family: str) -> dict[tuple[int, int], str]:
    family_path = OUT_DIR / f"{family}.rs"
    if family_path.exists():
        content = family_path.read_text()
        start = content.index("match (d, collision_l2_sq) {")
        end = content.index("_ => None,", start)
        return parse_arms(content[start:end])

    enum_name = FAMILY_ENUM[family]
    marker = f"SisModulusFamily::{enum_name} => match (d, collision_l2_sq) {{"
    start = content.index(marker)
    end = content.index("_ => None,", start)
    block = content[start:end]
    return parse_arms(block)


def merge_family_blocks(
    pow2_block: str | None,
    derived_block: str,
    existing: dict[tuple[int, int], str] | None = None,
) -> list[str]:
    merged: dict[tuple[int, int], str] = {}
    if existing is not None:
        merged.update(existing)
    if pow2_block is not None:
        merged.update(parse_arms(pow2_block))
    merged.update(parse_arms(derived_block))
    return [merged[key] for key in sorted(merged)]


def validate_rank_count(family: str, arm_lines: list[str], max_rank: int) -> None:
    for arm in arm_lines:
        match = ARM_RE.match(arm)
        if match is None:
            continue
        count = 0 if not match.group(3) else len(match.group(3).split(","))
        if count != max_rank:
            raise SystemExit(
                f"{family} row (d={match.group(1)}, collision={match.group(2)}) "
                f"has {count} widths; expected --max-rank {max_rank}"
            )


def stitch_family_file(family: str, arm_lines: list[str]) -> str:
    enum_name = FAMILY_ENUM[family]
    parts = [
        TABLE_HEADER,
        f"// Family: {enum_name}\n\n",
        "#[rustfmt::skip]\n",
        "pub(super) fn sis_max_widths(d: u32, collision_l2_sq: u128) -> Option<&'static [u64]> {\n",
        "    match (d, collision_l2_sq) {\n",
    ]
    for arm in arm_lines:
        parts.append(f"        {arm}\n")
    parts.append("        _ => None,\n")
    parts.append("    }\n")
    parts.append("}\n")
    return "".join(parts)


def stitch_mod() -> str:
    parts = [
        TABLE_HEADER,
        "\nmod q128;\nmod q32;\nmod q64;\n\n",
        "use super::SisModulusFamily;\n\n",
        "/// Generated SIS max-width table: for each `(family, d, collision_l2_sq)` the\n",
        "/// maximum secure ring-element width per module rank (`widths[rank - 1]`).\n",
        "#[rustfmt::skip]\n",
        "pub(crate) fn sis_max_widths(\n",
        "    family: SisModulusFamily,\n",
        "    d: u32,\n",
        "    collision_l2_sq: u128,\n",
        ") -> Option<&'static [u64]> {\n",
        "    match family {\n",
        "        SisModulusFamily::Q32 => q32::sis_max_widths(d, collision_l2_sq),\n",
        "        SisModulusFamily::Q64 => q64::sis_max_widths(d, collision_l2_sq),\n",
        "        SisModulusFamily::Q128 => q128::sis_max_widths(d, collision_l2_sq),\n",
        "    }\n",
        "}\n",
    ]
    return "".join(parts)


def write_table(family_arm_lines: dict[str, list[str]], *, dry_run: bool) -> None:
    if dry_run:
        print(stitch_mod())
        for family in FAMILIES:
            print(stitch_family_file(family, family_arm_lines[family]))
        return

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    (OUT_DIR / "mod.rs").write_text(stitch_mod())
    for family in FAMILIES:
        path = OUT_DIR / f"{family}.rs"
        path.write_text(stitch_family_file(family, family_arm_lines[family]))
        print(f"wrote {path} ({path.stat().st_size:,} bytes)", file=sys.stderr)
    if LEGACY_OUT.exists():
        LEGACY_OUT.unlink()
        print(f"removed legacy {LEGACY_OUT}", file=sys.stderr)


def split_existing_monolith() -> dict[str, list[str]]:
    content = LEGACY_OUT.read_text()
    return {
        family: [
            parse_existing_family_arms(content, family)[key]
            for key in sorted(parse_existing_family_arms(content, family))
        ]
        for family in FAMILIES
    }


def main() -> None:
    args = parse_args()

    if args.split_existing:
        if not LEGACY_OUT.exists():
            raise SystemExit(f"missing legacy table at {LEGACY_OUT}")
        family_arm_lines = split_existing_monolith()
        for family, arm_lines in family_arm_lines.items():
            validate_rank_count(family, arm_lines, args.max_rank)
        write_table(family_arm_lines, dry_run=args.dry_run)
        return

    derived_keys = derived_l2_collision_keys()
    existing_content = read_table_source()

    family_arm_lines: dict[str, list[str]] = {}
    for family in FAMILIES:
        if args.supplement_derived_only:
            if existing_content is None and not (OUT_DIR / f"{family}.rs").exists():
                raise SystemExit(f"missing existing table under {OUT_DIR}")
            source = existing_content or (OUT_DIR / "mod.rs").read_text()
            existing_arms = parse_existing_family_arms(source, family)
            derived_block = run_family(
                family, args.jobs, args.max_rank, collisions=derived_keys
            )
            family_arm_lines[family] = merge_family_blocks(
                None, derived_block, existing=existing_arms
            )
        else:
            derived_block = run_family(
                family, args.jobs, args.max_rank, collisions=derived_keys
            )
            pow2_block = run_family(family, args.jobs, args.max_rank)
            family_arm_lines[family] = merge_family_blocks(pow2_block, derived_block)

        validate_rank_count(family, family_arm_lines[family], args.max_rank)

    write_table(family_arm_lines, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
