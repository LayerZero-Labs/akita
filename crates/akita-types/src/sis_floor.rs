/// Return the minimum generated SIS rank that supports `width`.
pub fn min_rank_for_secure_width(
    family: SisModulusFamily,
    d: u32,
    collision_inf: u32,
    width: u64,
) -> Option<usize> {
    let widths = sis_max_widths(family, d, collision_inf)?;
    for (i, &max_w) in widths.iter().enumerate() {
        if width <= max_w {
            return Some(i + 1);
        }
    }
    None
}

/// Round `collision_inf` up to the next generated collision bucket.
pub fn ceil_supported_collision(
    family: SisModulusFamily,
    d: u32,
    collision_inf: u32,
) -> Option<u32> {
    const FULL: &[u32] = &[2, 3, 7, 15, 31, 63, 127, 255, 511, 1023, 2047];
    const Q128_D64: &[u32] = &[2, 3, 7, 15, 31, 63, 127, 255, 511, 1023, 2047, 4095, 8191];
    let buckets = match family {
        SisModulusFamily::Q16 => match d {
            32 | 64 | 128 | 256 => FULL,
            _ => return None,
        },
        SisModulusFamily::Q32 => match d {
            32 | 64 | 128 | 256 => FULL,
            _ => return None,
        },
        SisModulusFamily::Q64 => match d {
            32 | 64 | 128 | 256 => FULL,
            _ => return None,
        },
        SisModulusFamily::Q128 => match d {
            64 => Q128_D64,
            32 | 128 | 256 => FULL,
            _ => return None,
        },
    };
    buckets
        .iter()
        .copied()
        .find(|&bucket| collision_inf <= bucket)
}

